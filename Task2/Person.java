public class Person {
    
    String ID;
    String name;
    String surname;
 
    
    //Setters
    public void setID(String iD) {
        ID = iD;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    //Getters
    public String getID() {
        return ID;
    }

    public String getName() {
        return name;
    }
    
    public String getSurname() {
        return surname;
    }
}
